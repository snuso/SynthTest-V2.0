package sample;

public class ButtonModel {
    private String text;
    private int x;
    private int y;
    private int width;
    private int height;

    // text
    public String getText() {
        return text;
    }

    public void setText(String text) {
        this.text = text;
    }

    // x
    public int getX() {
        return x;
    }

    public void setX(int x) {
        this.x = x;
    }

    // y
    public int getY() {
        return y;
    }

    public void setY(int y) {
        this.y = y;
    }

    // width
    public int getWidth() {
        return width;
    }

    public void setWidth(int width) {
        this.width = width;
    }

    // height
    public int getHeight() {
        return height;
    }

    public void setHeight(int height) {
        this.height = height;
    }
}